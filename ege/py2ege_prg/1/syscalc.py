n = int( "255" ) # число 255
print( n )

n1 = int( "11111111", 2 ) # число 255
n2 = int( "377", 8 ) 		# число 255
n3 = int( "FF", 16 ) 		# число 255
print( n1, n2, n3 )

s2 = bin(255) 	# "0b11111111"
s8 = oct(255)		# "0o377"
s16 = hex(255)	# "0xff"
print( s2, s8, s16 )

s2 = bin(255)[2:] 		# "11111111"
s8 = oct(255)[2:]		# "377"
s16 = hex(255)[2:]		# "ff"
print( s2, s8, s16 )

s2 = f"{255:b}" 		# "11111111"
s8 = f"{255:o}"		    # "377"
s16 = f"{255:x}"		# "ff"
S16 = f"{255:X}"		# "FF"
print( s2, s8, s16, S16 )

s2 = f"{255:010b}" 		# "0011111111"
s8 = f"{255:05o}"		# "00377"
s16 = f"{255:06x}"		# "0000ff"
S16 = f"{255:04X}"		# "00FF"
print( s2, s8, s16, S16 )