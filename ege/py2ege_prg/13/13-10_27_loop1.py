with open("i/input_10.txt") as F:
  N = int( F.readline() )
  data = [ int(s) for s in F ]

def dist( i, j ):
  d1 = abs( i - j )
  return min( d1, N - d1 )

minCost = float('inf')
for i in range(N):
  cost = 0
  for j in range(N):
    cost += data[j] * dist(i, j)
  minCost = min( cost, minCost )

print( minCost )